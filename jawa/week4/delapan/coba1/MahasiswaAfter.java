
public class MahasiswaAfter {
  private int nrp;
  private String nama;

  public MahasiswaAfter(int nrp, String nama){
    this.nrp = nrp;
    this.nama = nama;
  }

  public int getNrp(){
    return this.nrp;
  }

  public int getNama(){
    return this.nrp;
  }

  public void setNrp(){
    this.nrp = nrp;
  }

  public void setNama(){
    this.nama = nama;
  }
}
